def evaluate_assessment(grades, gpa, field, stage):
    """
    Evaluates the assessment and returns perfectly matched JSON structure for the frontend.
    """
    sorted_improvement_plan = get_sorted_improvement_plan(grades, stage)
    majors_data = get_eligible_majors(field, gpa)
    learning_plan = generate_learning_plan(grades, gpa)

    return {
        "sortedImprovementPlan": sorted_improvement_plan,
        "allFieldMajors": majors_data["all_field_majors"],
        "eligibleMajors": majors_data["eligible_majors"],
        "learningPlan": learning_plan,
        "field": field,
        "fieldName": get_field_name(field),
        "gpa": gpa,
        "stage": stage,
        "hasFailedSubject": len([s for s in grades.values() if int(s) < 50]) > 0
    }
