def get_sorted_improvement_plan(grades, stage="senior"):
    items = []
    for k, s in grades.items():
        if s is not None and s != "":
            items.append(build_improvement_item(k, s, stage))
    return sorted(items, key=lambda x: x["score"])

def get_eligible_majors(field, gpa):
    gpa = float(gpa)
    field_majors = majors_db.get(field, [])
    
    all_field_majors = []
    for m in field_majors:
        all_field_majors.append({
            **m,
            "eligible": gpa >= m["minGPA"],
            "competitive": m["minGPA"] >= 80
        })
    
    # Sort by eligible desc, minGPA asc
    def sort_key(m):
        return (not m["eligible"], m["minGPA"])
    
    all_field_majors.sort(key=sort_key)
    
    eligible = [m for m in all_field_majors if m["eligible"]]
    return {
        "all_field_majors": all_field_majors,
        "eligible_majors": eligible
    }

