// ================================================================
// خوارزمية تحديد الحقل الأكاديمي — مُحسَّنة ودقيقة
// ================================================================
export function computeFieldFromRIASEC(sortedScores) {
  const top  = sortedScores[0][0]
  const sec  = sortedScores[1][0]
  const thrd = sortedScores[2][0]
  const code = top + sec

  // خريطة الأزواج الأساسية
  const pairMap = {
    // الصحي: I و S و R
    'IS': 'Health',  'SI': 'Health',
    'SR': 'Health',  'RS': 'Health',
    'IC': 'Health',  // علوم طبية مخبرية

    // الهندسي والتكنولوجي: R و I و C
    'RC': 'EngineeringIT',
    'CR': 'EngineeringIT',

    // الأعمال: E و C و R
    'EC': 'Business',  'CE': 'Business',
    'ER': 'Business',  'RE': 'Business',

    // الإنساني والاجتماعي: S و A و E
    'SA': 'Humanities',  'AS': 'Humanities',
    'AE': 'Humanities',  'EA': 'Humanities',
    'AC': 'Humanities',  'CA': 'Humanities',
    'SC': 'Humanities',  'CS': 'Humanities',
    'AI': 'Humanities',  'IA': 'Humanities',
  }

  if (pairMap[code]) {
    return pairMap[code]
  }

  // حالات غامضة → استخدام البُعد الثالث للتدقيق
  if (code === 'RI' || code === 'IR') {
    if (thrd === 'S') return 'Health'
    return 'EngineeringIT'
  }
  if (code === 'IE' || code === 'EI') {
    if (thrd === 'S') return 'Humanities'
    return 'Business'
  }
  if (code === 'SE' || code === 'ES') {
    if (thrd === 'A') return 'Humanities'
    return 'Business'
  }
  if (code === 'IS' || code === 'SI') {
    return 'Health'
  }

  // Fallback نهائي
  const fallback = {
    R: 'EngineeringIT',
    I: 'Health',
    A: 'Humanities',
    S: 'Humanities',
    E: 'Business',
    C: 'Business'
  }
  return fallback[top] || 'Business'
}


// ================================================================
// توليد أفضل 3 تخصصات مع نسبة تطابق وسبب مُخصَّص
// ================================================================
export function computeTopMajors(scores, fieldKey) {
  const field = JORDANIAN_FIELDS[fieldKey]
  if (!field) return []

  const WEIGHTS = { interest: 1.0, aptitude: 1.5, style: 1.2 }

  function dimMax(dim) {
    return riasecQuestions
      .filter(q => q.dimension === dim)
      .reduce((s, q) => s + 5 * (WEIGHTS[q.type] ?? 1.0), 0)
  }

  function dimPct(dim) {
    const mx = dimMax(dim)
    return mx > 0 ? (scores[dim] || 0) / mx : 0
  }

  // درجة تطابق التخصص: وسط مرجَّح لأبعاد RIASEC الثلاثة
  function majorMatchScore(riasecFit) {
    const dims = riasecFit.split('')
    const weights = [0.50, 0.32, 0.18]
    return dims.reduce((sum, d, i) => sum + dimPct(d) * (weights[i] || 0.10), 0)
  }

  // تحويل النسبة الخام (0-1) إلى نسبة عرض معقولة (65–98)
  function normalizeToDisplay(raw) {
    const pct = Math.round(raw * 100)
    if (pct >= 85) return Math.min(98, 85 + Math.round((pct - 85) * 0.60))
    if (pct >= 60) return 65 + Math.round((pct - 60) * 0.80)
    return Math.max(58, 50 + Math.round(pct * 0.30))
  }

  return field.topMajors
    .map(major => ({
      name:     major.name,
      matchPct: normalizeToDisplay(majorMatchScore(major.riasecFit)),
      minGPA:   major.minGPA,
      reason:   major.reason,
    }))
    .sort((a, b) => b.matchPct - a.matchPct)
    .slice(0, 3)
    .map((m, idx) => ({
      ...m,
      matchPct: Math.max(58, m.matchPct - idx * 4), // تفاوت واضح بين التخصصات
      rank: idx + 1,
    }))
}


